# Se usa el comando 'type' para saber q función es
print("type")
print(type([10,"hello world", True, 5.24]))
print("")

# Se usa el simbolo + para concatenar 2 o más palabras
print("Concatenación")
print('Bye'+' world')
print("")

# Integer 'int' para números enteros
print("int = suma y resta de enteros")
print(int(45))
print(int(5+8.24))
print("")

# Float 'float' para números decimales
print("float = operaciones si quieres decimales")
print(float(2.6))
print("")

# Boolean 'bool' para valores booleanos (v o f)
print("bool = pa ver si algo es cierto o no")
print(bool(5<85)) #true
print(bool(8<4)) #false 
print("")

# List 'list' puedo crear listas con cualquier tipo de datos q hemos visto antes
print("list = pa crear listas con cualquier tipo de dato")
print(list([10,"hello world", True, 5.24]))
print(list([]))
print("")

# Tuples 'tuple' puedo crear lista de datos pero q no pueden cambiar
print("tuple = lo mismo q list pero con datos inmutables")
print(tuple((10,"Hello World", False, 5+6.24)))
print(tuple(()))
print("")

# Dictionaries agrupar datos q pertenecen a una misma entidad
#   "Key":"Value"
print("dict = agrupar datos q pertenecen a una misma entidad")
print(dict({
    "Name":"Ryan",  
    "Lastname":"Ray",
    "Nickname":"Fazst"
}))
print(dict({}))
print("")
